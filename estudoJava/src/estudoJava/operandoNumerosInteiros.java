package estudoJava;

public class operandoNumerosInteiros {

	public static void main(String[] args) {
		
		/*média com números inteiros*/
		
		int nota1 = 90;
		int nota2 = 70;
		int nota3 = 50;
		int nota4 = 88;
		
		System.out.println("Média das notas = " + (nota1 + nota2 + nota3 + nota4) / 4);

	}

}
