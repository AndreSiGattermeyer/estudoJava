package estudoJava;

public class operandoNumerosDecimais {
	
public static void main(String[] args) {
		
		/*média com números decimais*/
		
		double nota1 = 90;
		double nota2 = 70;
		double nota3 = 50;
		double nota4 = 88;
		
		double mediaFinal = (nota1 + nota2 + nota3 + nota4) / 4;
		
		System.out.println("Média das notas é " + mediaFinal);

	}


}
